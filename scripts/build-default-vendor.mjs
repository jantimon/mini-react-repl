#!/usr/bin/env node
/**
 * Builds the default vendor bundle inlined as base64 data URLs, plus the
 * paired `.d.ts` payload (`vendor.types`) for editors that consume types.
 *
 * Output: two sibling files, both re-generated each run and gitignored:
 *   - `src/vendor-default/data.ts`        — import-map (always loaded)
 *   - `src/vendor-default/data-types.ts`  — `.d.ts` payload, dynamically
 *                                           imported only when an editor
 *                                           mounts
 *
 * Subsequent `tsup` build reads them and bakes them into the dist subpath;
 * `splitting: true` keeps the types payload in its own code-split chunk so
 * consumer bundlers can defer its download to the moment Monaco asks for it.
 *
 * Manifest lives at `src/default-vendor.ts` — same shape end users author
 * for their own custom vendor (`vendor.ts`).
 *
 * Imports the `.ts` source directly: requires Node ≥ 22.6 (with
 * --experimental-strip-types) or Node ≥ 23 (default-on).
 */

import { writeFile, mkdir } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { build } from '../src/vendor-builder/build.ts';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

const result = await build({
  entry: resolve(root, 'src/default-vendor.ts'),
  format: 'inline',
  types: 'embed',
  nodeEnv: 'development',
  cwd: root,
});

let totalBytes = 0;
for (const [spec, url] of Object.entries(result.importMap.imports)) {
  const code = Buffer.from(url.replace(/^data:text\/javascript;base64,/, ''), 'base64').toString(
    'utf8',
  );
  totalBytes += code.length;
  process.stdout.write(`  ${spec.padEnd(28)} ${(code.length / 1024).toFixed(1).padStart(7)} kB\n`);
}

const types = result.types ?? { libs: [] };
const typesBytes = types.libs.reduce((n, l) => n + l.content.length, 0);
process.stdout.write(
  `\n  ${types.libs.length} .d.ts files, ${(typesBytes / 1024).toFixed(1)} kB total\n\n`,
);

const dataOut = `// AUTO-GENERATED by scripts/build-default-vendor.mjs — do not edit.
/* eslint-disable */
import type { ImportMap } from '../types.ts'

export const DEFAULT_VENDOR_IMPORT_MAP: ImportMap = ${JSON.stringify(result.importMap, null, 2)}
`;

const typesOut = `// AUTO-GENERATED by scripts/build-default-vendor.mjs — do not edit.
/* eslint-disable */
import type { TypeBundle } from '../types.ts'

const DEFAULT_VENDOR_TYPES: TypeBundle = ${JSON.stringify(types)}

export default DEFAULT_VENDOR_TYPES
`;

const outDir = resolve(root, 'src/vendor-default');
await mkdir(outDir, { recursive: true });
const dataPath = resolve(outDir, 'data.ts');
const typesPath = resolve(outDir, 'data-types.ts');
await writeFile(dataPath, dataOut, 'utf8');
await writeFile(typesPath, typesOut, 'utf8');

console.log(
  `✓ default vendor written: ${(totalBytes / 1024).toFixed(1)} kB JS (data.ts), ${(typesBytes / 1024).toFixed(1)} kB types (data-types.ts)`,
);
