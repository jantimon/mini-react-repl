#!/usr/bin/env node
/**
 * Builds the default vendor bundle inlined as base64 data URLs, plus the
 * paired `.d.ts` payload (`vendor.types`) for editors that consume types.
 *
 * Output: `src/vendor-default/data.ts` (re-generated each run; gitignored).
 * Subsequent `tsup` build reads it and bakes it into the dist subpath.
 *
 * Manifest lives at `src/default-vendor.ts` — same shape end users author
 * for their own custom vendor (`vendor.ts`).
 *
 * Imports the `.ts` source directly: requires Node ≥ 22.6 (with
 * --experimental-strip-types) or Node ≥ 23 (default-on).
 */

import { writeFile, mkdir } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { build } from '../src/vendor-builder/build.ts';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

const result = await build({
  entry: resolve(root, 'src/default-vendor.ts'),
  format: 'inline',
  types: 'embed',
  nodeEnv: 'development',
  cwd: root,
});

let totalBytes = 0;
for (const [spec, url] of Object.entries(result.importMap.imports)) {
  const code = Buffer.from(url.replace(/^data:text\/javascript;base64,/, ''), 'base64').toString(
    'utf8',
  );
  totalBytes += code.length;
  process.stdout.write(`  ${spec.padEnd(28)} ${(code.length / 1024).toFixed(1).padStart(7)} kB\n`);
}

const types = result.types ?? { libs: [] };
const typesBytes = types.libs.reduce((n, l) => n + l.content.length, 0);
process.stdout.write(
  `\n  ${types.libs.length} .d.ts files, ${(typesBytes / 1024).toFixed(1)} kB total\n\n`,
);

const out = `// AUTO-GENERATED by scripts/build-default-vendor.mjs — do not edit.
/* eslint-disable */
import type { VendorBundle } from '../types.ts'

export const DEFAULT_VENDOR: VendorBundle = {
  importMap: ${JSON.stringify(result.importMap, null, 2)},
  types: ${JSON.stringify(types)},
}
`;

const outPath = resolve(root, 'src/vendor-default/data.ts');
await mkdir(dirname(outPath), { recursive: true });
await writeFile(outPath, out, 'utf8');

console.log(
  `✓ default vendor written to ${outPath} (${(totalBytes / 1024).toFixed(1)} kB JS, ${(typesBytes / 1024).toFixed(1)} kB types)`,
);
